package bbr.b2b.portal.classes.wrappers.management;

import bbr.b2b.users.report.classes.AuditPopUpTitleDTO;

public class PopupAuditFilterSectionInfo
{
	// =====================================================================================================================================
	// BEGINNING SECTION ----> PROPERTIES
	// =====================================================================================================================================
	private Integer				year	= null;
	private AuditPopUpTitleDTO	title	= null;
	private String				action	= null;

	// =====================================================================================================================================
	// ENDING SECTION ----> PROPERTIES
	// =====================================================================================================================================

	// =====================================================================================================================================
	// BEGINNING SECTION ----> CONSTRUCTORS
	// =====================================================================================================================================
	public PopupAuditFilterSectionInfo(Integer year, AuditPopUpTitleDTO title, String action)
	{
		this.year = year;
		this.title = title;
		this.action = action;
	}

	// =====================================================================================================================================
	// ENDING SECTION ----> CONSTRUCTORS
	// =====================================================================================================================================

	// =====================================================================================================================================
	// BEGINNING SECTION ----> PRIVATE METHODS
	// =====================================================================================================================================
	public Integer getYear()
	{
		return year;
	}

	public void setYear(Integer year)
	{
		this.year = year;
	}

	public AuditPopUpTitleDTO getTitle()
	{
		return title;
	}

	public void setTitle(AuditPopUpTitleDTO title)
	{
		this.title = title;
	}

	public String getAction()
	{
		return action;
	}

	public void setAction(String action)
	{
		this.action = action;
	}
	// =====================================================================================================================================
	// ENDING SECTION ----> PRIVATE METHODS
	// =====================================================================================================================================

}
